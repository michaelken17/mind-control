export function depressionSeverity(rawScoreGet) {
  const conversionData = DepressionConversionArr.filter(
    (x) => x.rawScore == rawScoreGet
  );
  const tScore = conversionData[0].tScore;

  // None to Slight
  if (tScore < 55) {
    return 0;
  }
  // Mild
  else if (tScore >= 55.0 && tScore <= 59.9) {
    return 1;
  }
  // Moderate
  else if (tScore >= 60.0 && tScore <= 69.9) {
    return 2;
  }
  // Severe
  else if (tScore >= 70) {
    return 3;
  }
}

